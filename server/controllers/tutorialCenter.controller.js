const supabase = require('../supabaseClient');
const { logger } = require('../utils/logger');

// Create tutorial center
exports.createCenter = async (req, res) => {
  try {
    const { name, description } = req.body;
    const tutorId = req.user.id;

    // Check if tutor already has a center
    const { data: existing } = await supabase
      .from('tutorial_centers')
      .select('id')
      .eq('tutor_id', tutorId)
      .single();

    if (existing) {
      return res.status(400).json({
        success: false,
        error: 'You already have a tutorial center'
      });
    }

    // Create center (access_code auto-generated by trigger)
    const { data, error } = await supabase
      .from('tutorial_centers')
      .insert([{ tutor_id: tutorId, name, description }])
      .select()
      .single();

    if (error) throw error;

    logger.info('Tutorial center created', { centerId: data.id, tutorId });
    res.json({ success: true, center: data });
  } catch (error) {
    logger.error('Create center error:', error);
    res.status(500).json({ success: false, error: error.message });
  }
};

// Get tutor's center
exports.getMyCenter = async (req, res) => {
  try {
    const tutorId = req.user.id;

    const { data, error } = await supabase
      .from('tutorial_centers')
      .select(`
        *,
        student_count:tc_enrollments(count),
        question_count:tc_questions(count),
        test_count:tc_question_sets(count)
      `)
      .eq('tutor_id', tutorId)
      .single();

    if (error && error.code !== 'PGRST116') throw error;

    res.json({ success: true, center: data });
  } catch (error) {
    logger.error('Get center error:', error);
    res.status(500).json({ success: false, error: error.message });
  }
};

