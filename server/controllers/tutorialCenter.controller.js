const supabase = require('../supabaseClient');
const { logger } = require('../utils/logger');

// Create tutorial center
exports.createCenter = async (req, res) => {
  try {
    const { name, description } = req.body;
    const tutorId = req.user.id;

    // Check if tutor already has a center
    const { data: existing } = await supabase
      .from('tutorial_centers')
      .select('id')
      .eq('tutor_id', tutorId)
      .single();

    if (existing) {
      return res.status(400).json({
        success: false,
        error: 'You already have a tutorial center'
      });
    }

    // Create center (access_code auto-generated by trigger)
    const { data, error } = await supabase
      .from('tutorial_centers')
      .insert([{ tutor_id: tutorId, name, description }])
      .select()
      .single();

    if (error) throw error;

    logger.info('Tutorial center created', { centerId: data.id, tutorId });
    res.json({ success: true, center: data });
  } catch (error) {
    logger.error('Create center error:', error);
    res.status(500).json({ success: false, error: error.message });
  }
};

// Get tutor's center
exports.getMyCenter = async (req, res) => {
  try {
    const tutorId = req.user.id;

    const { data, error } = await supabase
      .from('tutorial_centers')
      .select(`
        *,
        student_count:tc_enrollments(count),
        question_count:tc_questions(count),
        test_count:tc_question_sets(count)
      `)
      .eq('tutor_id', tutorId)
      .single();

    if (error && error.code !== 'PGRST116') throw error;

    res.json({ success: true, center: data });
  } catch (error) {
    logger.error('Get center error:', error);
    res.status(500).json({ success: false, error: error.message });
  }
};

// Update center
exports.updateCenter = async (req, res) => {
  try {
    const { name, description } = req.body;
    const tutorId = req.user.id;

    const { data, error } = await supabase
      .from('tutorial_centers')
      .update({ name, description })
      .eq('tutor_id', tutorId)
      .select()
      .single();

    if (error) throw error;

    res.json({ success: true, center: data });
  } catch (error) {
    logger.error('Update center error:', error);
    res.status(500).json({ success: false, error: error.message });
  }
};

// Get center students
exports.getCenterStudents = async (req, res) => {
  try {
    const tutorId = req.user.id;

    // Get center first
    const { data: center } = await supabase
      .from('tutorial_centers')
      .select('id')
      .eq('tutor_id', tutorId)
      .single();

    if (!center) {
      return res.status(404).json({ success: false, error: 'Center not found' });
    }

    // Get enrolled students with stats
    const { data, error } = await supabase
      .from('tc_enrollments')
      .select(`
        id,
        enrolled_at,
        student:student_id (
          id,
          email,
          raw_user_meta_data
        )
      `)
      .eq('center_id', center.id);

    if (error) throw error;

    // Format response
    const students = data.map(e => ({
      id: e.student.id,
      email: e.student.email,
      name: e.student.raw_user_meta_data?.name || e.student.email.split('@')[0],
      enrolled_at: e.enrolled_at
    }));

    res.json({ success: true, students });
  } catch (error) {
    logger.error('Get students error:', error);
    res.status(500).json({ success: false, error: error.message });
  }
};

module.exports = exports;
